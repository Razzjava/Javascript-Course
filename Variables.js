
// Variables

// old way of declaring a variable
var name ="razz"

// let can be updated
let name2 = "razz"

console.log(name2)

name2 = "james"
console.log(name2)

// const cannot be updated
const name3 = "razz"

// Data Types

// String: are text, wrapped in quotes
let lastName = "smith"
console.log(typeof lastName)

//number: can be integers, decimals, and more
let age = 28
console.log(typeof age)

//boolean:  true or false
let isSubscribed = false
console.log(typeof isSubscribed)

//undefined: means the variable has been declared but not given a value
let hello;
console.log(typeof hello)

// null type: represents an empty value on purpose
let empty = null;
console.log(typeof empty)
