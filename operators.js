// operators

let a = 10
let b = 5

// add
console.log(a+b)

// minus
console.log(a-b)

// mutiply
console.log(a*b)

// divide
console.log(a/b)

// modulus

console.log(a % b)

// increment
a++
console.log(a)

// decrement
a--
console.log(a)

// assignment
a += 10
console.log(a)

// Comparision Operators
let x = 10
let y = "10"

// equality
console.log(x == y)

// strict equality

console.log(x === y)

// not equals
console.log(x != y)

// not equals (strict)
console.log(x !== y)

// greater than
console.log(x >= 5)

// less than
console.log(x <= 5)